    public String convertirDecimalABinarioManual(long decimal) {
	if (decimal <= 0) {
		return "0";
	}
	StringBuilder binario = new StringBuilder();
	while (decimal > 0) {
            
		short residuo = (short) (decimal % 2);
		decimal = decimal / 2;
		binario.insert(0, String.valueOf(residuo));
	}
	return binario.toString();
    }
    
